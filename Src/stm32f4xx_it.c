/**
  * @brief This function handles TIM3 global interrupt.
  */
void TIM3_IRQHandler(void)
{
  /* USER CODE BEGIN TIM3_IRQn 0 */
	static uint32_t tim = 0;

  /* USER CODE END TIM3_IRQn 0 */
  HAL_TIM_IRQHandler(&htim3);
  /* USER CODE BEGIN TIM3_IRQn 1 */
  static int i = 0;
  static uint8_t dir = 1;

    ENGINE_SetSpeed(ENGINE_FL,i/1000);
  	ENGINE_SetSpeed(ENGINE_FR,i/1000);
  	ENGINE_SetSpeed(ENGINE_TL,i/1000);
  	ENGINE_SetSpeed(ENGINE_TR,i/1000);
  	ENGINE_SetSpeed(ENGINE_RL,i/1000);
  	ENGINE_SetSpeed(ENGINE_RR,i/1000);
  	ENGINE_SetSpeed(ENGINE_7,i/1000);
  	ENGINE_SetSpeed(ENGINE_8,i/1000);

  	if(dir && i>=100000) dir = 0;
  	if(!dir && i<=0) dir = 1;

  	if(dir) i++;
  	else i--;

//    PID_CalcOut(&PID_RegRoll, PID_RollRequested, ypr[0]);
//    PID_CalcOut(&PID_RegPitch, PID_PitchRequested, ypr[1]);
//    PID_CalcOut(&PID_RegYaw, PID_YawRequested, ypr[2]);
//
//    PID_SetEnginePowers(PID_RegRoll.y_pid, PID_RegPitch.y_pid, PID_RegYaw.y_pid, PID_XRequested, PID_ZRequested);

  	if(tim==0)
  	{
  		CAN_PARSER_PrepareWholeFifo();
  	}

    if(!CAN_PARSER_TXFifoBusy)
    {
    	CAN_PARSER_TXFifoBusy = true;
    	CAN_PARSER_SendMessageFromFifo();
    	CAN_PARSER_TXFifoDelayCounter = CAN_PARSER_TX_FIFO_DELAY;
    }
    else
    {
    	if(CAN_PARSER_TXFifoDelayCounter == 0)CAN_PARSER_SendMessageFromFifo();
    	else CAN_PARSER_TXFifoDelayCounter--;
    }
  	  	tim++;
  	  	tim = tim%CAN_PARSER_TX_PERIOD;


  /* USER CODE END TIM3_IRQn 1 */
}

